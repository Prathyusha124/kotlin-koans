fun main(args: Array<String>) {
    var i:Number=12.2
    print(i)
    var j:Int=10
    println(j)
    var k:Float=25.78f
    println(k)
    j=k.toInt()
    println(j)
    var str:String="Hello world!"
    println("Length=${str.length}")
    var bool=true
    println(bool)
    var ch:Char='d'
    println(ch)
}